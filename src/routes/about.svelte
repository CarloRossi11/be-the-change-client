<h1>About Be The Change</h1>

<p>Be The Change is a platform where users can easily find volunteer opportunities in their area and filter the results by opportunity type. Organizations and individuals can register to post volunteer opportunites that they will be hosting</p>

<p>BTC was developed by Carlo Berardelli in December 2020. Inspiration came from an idea shared with him by Dr. Shane Kelly. Please reach out to Carlo with any questions or comments </p>

